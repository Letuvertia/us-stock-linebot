def main():
    svc = common.get_sheets_service()

    # Read all rows
    result = svc.spreadsheets().values().get(
        spreadsheetId=USER_CONFIG_ID,
        range='News Keywords!A:E'
    ).execute()
    rows = result.get('values', [])

    if not rows or rows[0][0] != 'Ticker':
        print('Unexpected header row:', rows[0] if rows else '(empty)')
        return

    # Build update data: list of (row_index_1based, chinese_name)
    updates = []
    missing = []
    for i, row in enumerate(rows[1:], start=2):  # 1-indexed, row 1 is header
        ticker = row[0].strip() if row else ''
        if not ticker:
            continue
        name = CHINESE_NAMES.get(ticker)
        if name:
            value = ', '.join(name) if isinstance(name, list) else name
            updates.append({'range': f'News Keywords!E{i}', 'values': [[value]]})
        else:
            missing.append(ticker)

    print(f'Will update {len(updates)} tickers, missing {len(missing)}: {missing}')

    # Batch update
    if updates:
        body = {'valueInputOption': 'RAW', 'data': updates}
        svc.spreadsheets().values().batchUpdate(
            spreadsheetId=USER_CONFIG_ID, body=body
        ).execute()
        print('Done.')


if __name__ == '__main__':
    main()
